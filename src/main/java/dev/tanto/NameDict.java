package dev.tanto;

public class NameDict {
    String huName;
    String itName;
    public NameDict() {}
    public NameDict(String huName, String itName) {
        this.huName = huName;
        this.itName = itName;
    }
    public String getHuName() {
        return huName;
    }
    public void setHuName(String huName) {
        this.huName = huName;
    }
    public String getItName() {
        return itName;
    }
    public void setItName(String itName) {
        this.itName = itName;
    }
    
    
}
